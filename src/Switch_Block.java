public class Switch_Block {
    public static void main(String[] args) {
        int month = 2;
        switch (month) {

            case 1:
                System.out.println("Januar");
                break;
            case 2:
                System.out.println("Februar");
                break;
            case 3:
                System.out.println("Marz");
                break;
            case 4:
                System.out.println("April");
                break;


        }
    }
}
