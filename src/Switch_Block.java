public class Switch_Block {
    public static void main(String[] args) {
        String month = "June";
        switch (month) {
            case "Januar":
                System.out.println(1);
                break;
            case "Februar":
                System.out.println(2);
                break;
            case "Marz":
                System.out.println(3);
                break;
            case "April":
                System.out.println(4);
                break;
            default:
                System.out.println(8);


        }


    }
}
